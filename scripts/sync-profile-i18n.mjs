const localeOverrides = {
  uk: ukProfileNav,
  en: enProfileNav,
  ru: ruProfileNav,
  tr: enProfileNav,
  fr: enProfileNav,
  cs: enProfileNav,
  es: enProfileNav,
  ar: enProfileNav,
};

for (const locale of ['uk', 'en', 'ru', 'tr', 'fr', 'cs', 'es', 'ar']) {
  const filePath = path.join(localesDir, `${locale}.json`);
  const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  const override = localeOverrides[locale];
  existing.navigation = { ...existing.navigation, ...override.navigation };
  existing.profile = { ...existing.profile, ...override.profile };
  fs.writeFileSync(filePath, `${JSON.stringify(existing, null, 2)}\n`);
  console.log('updated', locale);
}
