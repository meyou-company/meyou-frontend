/** Keep profile.posts in sync with posts action keys for legacy callers. */
function profilePostsMirror(posts) {
  return {
    createTitle: posts.createTitle,
    placeholder: posts.placeholder,
    placeholderWithName: posts.placeholderWithName,
    loading: posts.loading,
    empty: posts.empty,
    like: posts.actions.like,
    comment: posts.actions.comment,
    save: posts.actions.save,
    share: posts.actions.share,
  };
}

for (const locale of Object.keys(localeOverrides)) {
  const filePath = path.join(localesDir, `${locale}.json`);
  const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  const block = localeOverrides[locale];
  existing.feed = { ...existing.feed, ...block.feed };
  existing.posts = { ...existing.posts, ...block.posts };
  existing.comments = { ...existing.comments, ...block.comments };
  existing.profile = existing.profile || {};
  existing.profile.posts = {
    ...existing.profile.posts,
    ...profilePostsMirror(block.posts),
  };
  fs.writeFileSync(filePath, `${JSON.stringify(existing, null, 2)}\n`);
  console.log('updated', locale);
}
